// if else if statement
// we want to give remarks to a student based on his/her marks in CRE subject
void main() {
  var CRE = 7;
  if (CRE > 80) {
    print("Fantastic job");
  } 
  else if (CRE >= 70) {
    print("good job student!");
  } 
  else if (CRE > 50) {
    print("Average and good job student!");
  } 
  else {
    print("Attend remedial classes");
  }
}
